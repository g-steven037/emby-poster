# CRON_SCHEDULE: 容器后台定时运行的 Cron 表达式。
# 如果留空 ("")，则脚本仅执行一次后自动退出。
# 如果填写标准的 Cron 表达式，容器将保持运行，并在指定时间触发更新。
# 常用的 Cron 表达式示例：
# "0 * * * *"      -> 每小时的第 0 分钟执行一次
# "0 2 * * *"      -> 每天凌晨 2:00 执行一次
# "0 2,14 * * *"   -> 每天凌晨 2:00 和 下午 14:00 各执行一次
# "0 3 * * 1"      -> 每周一凌晨 3:00 执行一次
